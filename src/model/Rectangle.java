package model;

public class Rectangle extends Figure {
    private int width;
    private int height;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    @Override
    public FigureType getType() {
        return FigureType.RECTANGLE;
    }
}
